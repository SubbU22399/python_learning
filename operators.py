a,b = 10, 2
print(a+b)  # addition
print(a-b)  # subtraction
print(a*b)  # multiplication 
print(a/b)  # division  (quotient)
print(a%b)  # modulus   (remainder)
print(a**b)  # exponent (power)
print(a==b)  # equal to
print(a!=b)  # not equal to
print(a>b)  # greater than
print(a//b)  # floor division  (quotient)
print(a<b)  # less than
print(a>=b)  # greater than or equal to
print(a<=b)  # less than or equal to
print(a and b)  # logical AND
print(a or b)  # logical OR
print(not a)  # logical NOT
print(not b)  # logical NOT
print(a&b)  # bitwise AND
print(a|b)  # bitwise OR    
print(a^b)  # bitwise XOR   
print(~a)  # bitwise NOT    
print(~b)  # bitwise NOT    
print(a<<b)  # bitwise left shift   
print(a>>b)  # bitwise right shift  
print(a is b)  # identity
print(a is not b)  # identity
